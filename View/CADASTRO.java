    //BOTÃƒO SALVAR
    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
       Produto pro = new Produto();
        produtoDAO dao = new produtoDAO();
                
//pegando o texto nos campo e guardado em suas variaveis
//      Integer id = (Integer.parseInt(TxtCadProdutoID.getText()));
        
        
        //setando os produtos no BANCO DE DADOS e salvando
         if (dao.CHECKPRODUTO(txtProduto.getText())) {
             JOptionPane.showMessageDialog(null, "Este Produto ja Foi Cadastrado!");
        }else{
        pro.setProduto(txtProduto.getText());
        pro.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
        pro.setValor(Double.parseDouble(txtPreco.getText()));
         }
       
        dao.SAVE(pro);//salva produto
        readTable();//lÃª na tabela
        
        
        //limpando campo de text
//        TxtCadProdutoID.setText("");
        txtProduto.setText("");
        txtQuantidade.setText("");
        txtPreco.setText("");
        printCadID.setText("");
        
        //retorna barra de texto ao locao pre definido
        txtProduto.requestFocus();  
    }//GEN-LAST:event_btnSalvarActionPerformed

    
