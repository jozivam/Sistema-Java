//Ler Tabela
    public void readTable(){
        DefaultTableModel TAB = (DefaultTableModel) tabCadastro.getModel();
        TAB.setNumRows(0);
        produtoDAO pdao = new produtoDAO();
        
        for (Produto pro: pdao.READ()) {
            
            TAB.addRow(new Object[]{
                pro.getId(),
                pro.getProduto(),
                pro.getQuantidade(), 
                pro.getValor()
            });
        }
    }
//FAZ PESQUISA NO BANCO
    public void readTableBuscar(String produto){
        DefaultTableModel TAB = (DefaultTableModel) tabCadastro.getModel();
        TAB.setNumRows(0);
        produtoDAO pdao = new produtoDAO();
        
        for (Produto pro: pdao.SEARCH(produto)) {
            
            TAB.addRow(new Object[]{
                pro.getId(),
                pro.getProduto(),
                pro.getQuantidade(),
                pro.getValor()
                
            });
        }
    }

